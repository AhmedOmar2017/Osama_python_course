#----------------------------------------
#------------- Comparison Operators -----
#----------------------------------------
# [==] equal
# [!=] not equal
# [>] greater than
# [<] less than
# [>=] greater than or equal
# [<=] less than or equal
#-----------------------------------------


# Equal and not equal
print(1000 ==  1000)
print(1000 == 100)

print("#" * 40)


print(100 != 1000)
print(1000 != 1000)

print("#" * 40)

#  greater than , less than

print(1000 <  1000)
print(1000 > 100)

print("#" * 40)


print(100 < 1000)
print(1000 > 1000)

print("#" * 40)

#  greater than or equal , less than or equal

print(1000 <=  1000)
print(1000 >= 100)

print("#" * 40)


print(100 <= 1000)
print(1000 >= 1000)

print("#" * 40)
